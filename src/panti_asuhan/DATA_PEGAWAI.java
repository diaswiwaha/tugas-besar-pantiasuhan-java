    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int table =jTable1.getSelectedRow();
        String a = jTable1.getValueAt(table, 0).toString(); //id
        String b = jTable1.getValueAt(table, 1).toString(); //nama
        String c = jTable1.getValueAt(table, 2).toString(); //jk
        String d = jTable1.getValueAt(table, 3).toString(); //tempatlahir
        //tgl lahir
        try{
            java.util.Date tgl= new SimpleDateFormat("yyyy-MM-dd").parse((String)jTable1.getValueAt(table,4));
            jDateChooser1.setDate(tgl);
        } catch (Exception se){
        }
        String f = jTable1.getValueAt(table, 5).toString();//alamat
        String g = jTable1.getValueAt(table, 6).toString();//nohp
        String h = jTable1.getValueAt(table, 7).toString();
        
        
        jTextField1.setText(a);
        jTextField2.setText(b);
        if("LAKI-LAKI".equals(c)){
            jRadioButton1.setSelected(true);
        }else{
            jRadioButton2.setSelected(true);
        }
        if("PEREMPUAN".equals(c)){
            jRadioButton2.setSelected(true);
        }else{
            jRadioButton1.setSelected(true);
        }
        jTextField3.setText(d);
        jTextPane1.setText(f);
        jTextField5.setText(g);
        jTextField6.setText(h);
        
        jTextField1.setEnabled(false);
        jTextField2.requestFocus();
    }//GEN-LAST:event_jTable1MouseClicked

