    private void jTextField7KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField7KeyReleased
        // TODO add your handling code here:
    DefaultTableModel tabel = new DefaultTableModel();
    tabel.addColumn("ID");
    tabel.addColumn("Nama");
    tabel.addColumn("Jenis Kelamin");
    tabel.addColumn("Tempat Lahir");
    tabel.addColumn("Tanggal Lahir");
    tabel.addColumn("Alamat");
    tabel.addColumn("No Hp");
    tabel.addColumn("Email");
    
    try{
        String sql="Select*from pegawai where id_pegawai like '%"+
                jTextField7.getText()+"%' or nama_pegawai like '%"+
                jTextField7.getText()+"%' or tempat_lahir like '%"+
                jTextField7.getText()+"%' or tanggal_lahir like '%"+
                jTextField7.getText()+"%' or jk like '%"+
                jTextField7.getText()+"%' or no_hp like '%"+
                jTextField7.getText()+"%' or alamat like '%"+
                jTextField7.getText()+"%' or email like '%"+
                jTextField7.getText()+"%'";
        Statement stt=konek.createStatement();
        ResultSet res=stt.executeQuery(sql);
        while(res.next()){
            tabel.addRow(new Object[]{
            res.getString("id_pegawai"),
            res.getString("nama_pegawai"),
            res.getString("jk"),
            res.getString("tempat_lahir"),
            res.getString("tanggal_lahir"),
            res.getString("alamat"),
            res.getString("no_hp"),
            res.getString("email"),});
            jTable1.setModel(tabel);
        }
    }
    catch (Exception e){
    }   
    }//GEN-LAST:event_jTextField7KeyReleased

